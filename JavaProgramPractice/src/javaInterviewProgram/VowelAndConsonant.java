package javaInterviewProgram;

public class VowelAndConsonant {

	public static void main(String[] args) 
	{
		// a,e.,i,o,u
		
		char ch ='b';
		
		if(ch=='a'||ch=='e'||ch=='i'||ch=='o'||ch=='u')
		{
			System.out.println("This is vowel: "+ch);
		}else
		{
			System.out.println("This is consonant: "+ch);
		}
		

	}

}
