package com.java.program;

public class While_DoWhile_Loop {

	public static void main(String[] args) 
	{
		int num=1;
		System.out.println("Output is:");	
		while(num<=10)
		{
			System.out.println(num);
			num++;
		}
		
		System.out.println("************************************");
		int i=1;
		do
		{
			System.out.println(" " + i +" ");
			i++;
		}
		while(i<=10);
	
	}

}
